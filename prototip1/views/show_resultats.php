<h2>Fonaments d'Informàtica 2019/2020</h2>
<br>
<p>1. Aquesta ha estat la primera vegada que m'he matriculat d'aquesta assignatura</p>
<p>2. La meva assistència a les activitats presencials d'aquesta assignatura ha estat</p>
<p>3. En mitjana, el nombre d'hores setmanals que he dedicat a aquesta assignatura, fora de les hores
presencials, ha estat</p>
<p>4. Fins ara s'ha seguit la programació de l'assignatura que s'explica en la Guia Docent</p>
<p>5. El material del curs (presentacions en classe, enunciats de problemes o casos, guions de seminaris i
pràctiques, lectures, bibliografia de consulta, etc.) està ben preparat i resulta útil</p>
<p>6. El sistema d'avaluació s'explica clarament a la Guia Docent de l'assignatura</p>
<p>7. Els continguts de les proves i d'altres activitats d'avaluació es corresponen amb els continguts del curs i
també amb el temps que el professorat va dedicar a cada tema</p>
<p>8. La càrrega de treball de l'estudiant està ben dimensionada</p>
<p>9. Amb aquesta assignatura estic aprenent coses que considero valuoses per a la meva formació</p>